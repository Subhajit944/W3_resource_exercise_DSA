    cout << " The sizeof(long int) is :          " << sizeof(long int) << " bytes \n" ;
    cout << " The sizeof(short int ) is :        " << sizeof(short int) << " bytes \n" ;
    cout << " The sizeof(long long int ) is :    " << sizeof(long long int) << " bytes \n" ;


